"公冶長之二四：子曰：「孰謂微生高直？或乞醯焉，乞諸其鄰而與之。」",

"公冶長之二五：子曰：「巧言、令色、足恭，左丘明恥之，丘亦恥之。匿怨而友其人，左丘明恥之，丘亦恥之。」",

"公冶長之二六：顏淵、季路侍。子曰：「盍各言爾志？」子路曰：「願車馬、衣輕裘，與朋友共，敝之而無憾。」顏淵曰：「願無伐善，無施勞。」子路曰：「願聞子之志。」子曰：「老者安之，朋友信之，少者懷之。」",

"公冶長之二七：子曰：「已矣乎！吾未見能見其（己）過，而內自訟者也。」",

"公冶長之二八：子曰：「十室之邑，必有忠信如丘者焉，不如丘之好學也。」",

"雍也之一：子曰：「雍（冉雍，字仲弓）也，可使南面。」仲弓問子桑伯子，子曰：「可也，簡」。仲弓曰：「居（平常修養的功夫）敬而行簡，以臨其民，不亦可乎？居簡而行簡，無乃大簡乎！」子曰：「雍之言然。」",

"雍也之二：哀公問：「弟子孰為好學？」孔子對曰：「有顏回者，好學；不遷怒，不貳過，不幸短命死矣（只要先於父母而死，就是短命）！今也則亡，未聞好學者也。」",

"雍也之三：子華使於齊，冉子為其母請粟。子曰：「與之釜（六斗四升）。」請益，曰：「與之庾（十六斗）。」冉子與之粟五秉（相當八百斗）。子曰：「赤之適齊也，乘肥馬，衣輕裘；吾聞之也：君子周急不繼富。」原思為之宰，與之粟九百，辭。子曰：「毋！以與爾鄰里鄉黨乎！」",

"雍也之四：子謂仲弓曰：「犁牛（耕田的牛）之子，騂且角（純赤色、牛角長得周正）；雖欲勿用，山川其舍諸（就算是犁牛之子而不拿來祭祀，山川之神難道會捨棄他麼）？」",

"雍也之五：子曰：「回也，其心三月不違仁，其餘，則日月至焉而已矣。」",

"雍也之六：季康子問：「仲由可使從政也與？」子曰：「由也果，於從政乎何有？」曰：「賜也可使從政也與？」曰：「賜也達，於從政乎何有！」曰：「求也可使從政也與？」曰：「求也藝，於從政乎何有？」",

"雍也之七：季氏使閔子騫為費宰。閔子騫曰：「善為我辭焉。如有復我者，則吾必在汶上矣。」",

"雍也之八：伯牛有疾，子問之，自牖執其手，曰：「亡之！命矣夫？斯人也，而有斯疾也！斯人也，而有斯疾也！」",

"雍也之九：子曰：「賢哉回也！一簞食，一瓢飲，在陋巷，人不堪其憂，回也不改其樂。賢哉回也！」",

"雍也之十：冉求曰：「非不說子之道，力不足也。」子曰：「力不足者，中道而廢（能力不足的人，在走到半途才不得不放棄）；今女畫（畫地自限）。」",

"雍也之十一：子謂子夏曰：「女為君子儒，無為小人儒。」",

"雍也之十二：子游為武城宰。子曰：「女得人焉耳乎？」曰：「有澹臺滅明者，行不由徑；非公事，未嘗至於偃之室也。」",

"雍也之十三：子曰：「孟之反不伐（伐，誇耀功勞），奔（戰敗逃跑）而殿（在軍隊後面拒敵），將入門，策其馬，曰：『非敢後也，馬不進也。』」",

"雍也之十四：子曰：「不有祝鮀之佞，而有宋朝之美，難乎免於今之世矣。」（臉長得好看為「貌」，整個體態都好看為「美」）",

"雍也之十五：子曰：「誰能出不由戶？何莫由斯道也！」",

"雍也之十六：子曰：「質勝文則野（樸實），文勝質則史。文質彬彬，然後君子。」",

"雍也之十七：子曰：「人之生也直；罔之生也幸而免。」",

"雍也之十八：子曰：「知之者，不如好之者，好之者，不如樂之者。」",

"雍也之十九：子曰：「中人以上，可以語上也；中人以下，不可以語上也。」",

"雍也之二十：樊遲問知，子曰：「務民之義，敬鬼（祖先）神（有遺愛在民者）而遠之，可謂知矣」（為了感念恩德所以敬鬼神，但不要為了有所求而過分祭祀，所以遠之）。問仁，曰：「仁者先難（該作的事）而後獲，可謂仁矣。」",

"雍也之二一：子曰：「知者樂水（逝者如斯夫，自強不息），仁者樂山（知其所止，止於至善）。知者動，仁者靜。知者樂（自強不息，樂此不疲），仁者壽（仁者的貢獻與天地同壽）。」",

"雍也之二二：子曰：「齊（齊國為政經強國）一變，至於魯（《春秋》之魯為文化大國）；魯一變，至於道（大道之行也，天下為公）。」",

"雍也之二三：子曰：「觚不觚（該有稜角的酒器沒稜角），（反諷）觚哉觚哉！」",

"雍也之二四：宰我問曰：「仁者雖告之曰：『井有仁焉（井裡有人）。』其從之（跳下去救）也？」子曰：「何為其然也？君子可逝（跑去救）也，不可陷（貿然跳下去）也。可欺（以有理欺之）也，不可罔（以無理欺之）也。」",

"雍也之二五：子曰：「君子博學於文（經天緯地謂之文），約之以禮，亦可以弗畔（不違背道；率性之謂道）矣夫！」",

"雍也之二六：子見南子，子路不說。夫子矢之曰：「予所否者，天厭之！天厭之！」",

"雍也之二七：子曰：「中庸之為德也，其至矣乎？民鮮久矣！」",

"雍也之二八：子貢曰：「如有博施於民，而能濟眾（問：拿什麼濟眾？），何如？可謂仁乎？」子曰：「何事於仁？必也聖乎？堯舜其猶病諸？夫仁者，己欲立而立人，己欲達而達人。能近取譬，可謂仁之方（分寸）也已。」",


"述而之一：子曰：「述（接著前人講，有本有源）而不作，信而好古，竊比於我老彭。」",

"述而之二：子曰：「默而識（心領神會）之，學而不厭（古論作學道不厭），誨人不倦，何有於我哉？」",

"述而之三：子曰：「德之不修，學之不講，聞義不能徙，不善不能改，是吾憂也。」",

"述而之四：子之燕居（燕居，在後廳居家生活，不見客；閒居，在二廳，能見客），申申如也，夭夭如也。",

"述而之五：子曰：「甚矣吾衰也！久矣，吾不復夢見周公（夢想實現周公之道）！」（孔子一生思想有三變）",

"述而之六：子曰：「志於道，據於德，依於仁，游於藝。」",

"述而之七：子曰：「自行束脩以上，吾未嘗無誨焉！」",

"述而之八：子曰：「不憤不啟；不悱不發；舉一隅不以三隅反，則不復也。」",

"述而之九：子食於有喪者之側，未嘗飽也。子於是日哭，則不歌。",

"述而之十：子謂顏淵曰：「用之則行（行什麼？），舍之則藏（藏在哪？）。惟我與爾有是夫！」",

"述而之十：子路曰：「子行三軍，則誰與？」子曰：「暴虎馮河，死而無悔者，吾不與也。必也臨事而懼，好謀而成者也。（中國學問要人成功，不是教人作烈士）」",

"述而之十一：子曰：「富而可求也，雖執鞭之士，吾亦為之；如不可求，從吾所好。」",

"述而之十二：子之所慎：齊，戰，疾。",

"述而之十三：子在齊，聞韶三月，不知肉味，曰：「不圖為樂（音樂）之至於斯也！」",

"述而之十四：冉有曰：「夫子為衛君乎？」子貢曰：「諾，吾將問之」。入曰：「伯夷叔齊，何人也？」曰：「古之賢人也。」曰：「怨乎？」曰：「求仁而得仁，又何怨？」出，曰：「子不為也。」",

"述而之十五：子曰：「飯疏食，飲水（冷水），曲肱而枕之，樂亦在其中矣。不義而富且貴，於我如浮雲。」",

"述而之十六：子曰：「加我數年，五十以學易，可以無大過矣。」（無意而犯錯，沒害到人，為小過。若害到人，為大過）",

"述而之十七：子所雅言：「詩、書、執禮，皆雅言也。」",

"述而之十八：葉公問孔子於子路，子路不對。子曰：「女奚不曰：『其為人也，發憤忘食，樂以忘憂，不知老之將至云爾。』」",

"述而之十九：子曰：「我非生而知之者，好古，敏（慮深通敏）以求之者也。」",

"述而之二十：子不語（閒聊）：怪、力、亂、神。",

"述而之二一：子曰：「三人行，必有我師焉。擇其善者而從之；其不善者而改之。」",

"述而之二二：子曰：「天生德於予，桓魋其如予何！」",

"述而之二三：子曰：「二三子，以我為隱乎？吾無隱乎爾！吾無行而不與二三子者，是丘也。」",

"述而之二四：子以四教：文（經緯天地謂之文）、行（能作到）、忠（盡己之謂忠）、信（有諸己之謂信）。",

"述而之二五：子曰：「聖人，吾不得而見之矣！得見君子者，斯可矣。」子曰：「善人，吾不得而見之矣！得見有恆者，斯可矣。」亡而為有，虛而為盈，約而為泰，難乎有恆矣！」",

"述而之二六：子釣而不綱，弋而不射宿。",

"述而之二七：子曰：「蓋有不知而作之者（不懂裝懂），我無是也。多聞，擇其善者而從之，多見而識之，知之次也（僅次於智慧了）。」（多聞闕疑，多見闕殆）",

"述而之二八：互鄉難與言。童子見，門人惑。子曰：「與（ㄩˋ，稱許）其進也，不與（ㄩˋ，稱許）其退也。唯，何甚！人絜（ㄧˋ，度量）己以進，與其絜（ㄧˋ，度量）也，不保其往也！」",

"述而之二九：子曰：「仁遠乎哉？我欲仁，斯仁至矣。」",

"述而之三十：陳司敗問：「昭公知禮乎？」孔子對曰：「知禮。」孔子退，揖巫馬期而進之，曰：「吾聞君子不黨，君子亦黨乎？君（魯公）取於吳為同姓，謂之吳孟子。君而知禮，孰不知禮？」巫馬期以告。子曰：「丘也幸，苟有過，人必知之。」",

"述而之三一：子與人歌而善，必使反之（再唱一次），而後和之。",

"述而之三二：子曰：「文莫（黽勉，勤勉努力），吾猶人也（我尚且要勤勉地行仁義）；躬行君子（安行，由仁義行），則吾未之有得！」",

"述而之三三：子曰：「若聖與仁，則吾豈敢？抑為之不厭，誨人不倦，則可謂云爾已矣！」公西華曰：「正唯弟子不能學也！」",

"述而之三四：子疾病（重病），子路請禱。子曰：「有諸？」子路對曰：「有之。誄曰：『禱爾于上下神祇。』」子曰：「丘之禱久矣！」",

"述而之三五：子曰：「奢則不孫，儉則固；與其不孫也，寧固。」",

"述而之三六：子曰：「君子坦蕩蕩，小人長戚戚。」",

"述而之三七：子溫而厲（勵），威而不猛，恭而安。",


"泰伯之一：子曰：「泰伯，其可謂至德也已矣！三以天下讓，民無得而稱焉。」",

"泰伯之二：子曰：「恭而無禮則勞，慎而無禮則葸（畏懼），勇而無禮則亂（悖亂），直而無禮則絞（把事情弄糟）。君子篤（有誠和情）於親（孝悌），則民興於仁。故舊不遺，則民不偷（薄情）。」",

"泰伯之三：曾子有疾，召門弟子曰：「晵予足！晵予手！詩云：『戰戰兢兢，如臨深淵，如履薄冰。』而今而後，吾知免夫？小子！」",

"泰伯之四：曾子有疾，孟敬子問之。曾子言曰：「鳥之將死，其鳴也哀，人之將死，其言也善。君子所貴乎道者三：動容貌，斯遠暴慢矣；正顏色，斯近信矣；出辭氣，斯遠鄙倍矣；籩豆之事，則有司存。」",

"泰伯之五：曾子曰：「以能問於不能，以多問於寡，有若無，實若虛，犯而不校。昔者吾友（顏回），嘗從事於斯矣。」",

"泰伯之六：曾子曰：「可以託六尺之孤，可以寄百里之命，臨大節而不可奪也。君子人與？君子人也！」",

"泰伯之七：曾子曰：「士不可不弘毅，任重而道遠。仁以為己任，不亦重乎；死而後已，不亦遠乎。」",

"泰伯之八：子曰：「興於詩，立於禮，成於樂。」",

"泰伯之九：子曰：「民可使由之，不可使知之。」",

"泰伯之十：子曰：「好勇疾貧，亂也。人而不仁，疾之已甚，亂也。」",

"泰伯之十一：子曰：「如有周公之才之美，使驕且吝，其餘不足觀也已！」",

"泰伯之十二：子曰：「三年學，不至於穀，不易得也。」",

"泰伯之十三：子曰：「篤信好學，守死善道。危邦不入，亂邦不居。天下有道則見，無道則隱。邦有道，貧且賤焉，恥也；邦無道，富且貴焉，恥也。」",

"泰伯之十四：子曰：「不在其位（位為己所立），不謀其政。」",

"泰伯之十五：子曰：「師摯之始，關雎之亂（終篇），洋洋乎，盈耳哉！」",

"泰伯之十六：子曰：「狂而不直，侗（不懂裝懂）而不愿，悾悾而不信，吾不知之矣！」",

"泰伯之十七：子曰：「學如不及，猶恐失之。」",

"泰伯之十八：子曰：「巍巍乎！舜、禹之有天下也，而不與焉。」",

"泰伯之十九：子曰：「大哉！堯之為君也。巍巍乎，唯天為大，唯堯則之（只有堯學天）。蕩蕩乎，民無能名焉！巍巍乎，其有成功也！煥乎，其有文章！」",

"泰伯之二十：舜有臣五人，而天下治。武王曰：「予有亂臣（治亂之臣）十人。」孔子曰：「才難，不其然乎？唐虞之際，於斯為盛，有婦人焉，九人而已。三分天下有其二，以服事殷、周之德，其可謂至德也已矣！」",

"泰伯之二一：子曰：「禹，吾無閒然矣（沒什麼好批評的）。菲飲食，而致孝乎鬼神；惡衣服，而致美乎黻冕；卑宮室，而盡力乎溝洫（水利工程）。禹，吾無間矣！」",


"子罕之一：子罕言利（利不是用說的，是實際的好處，再進一步能以美利利天下），與（稱許）命與（稱許）仁。",

"子罕之二：達巷黨人曰：「大哉孔子！博學而無所成名。」子聞之，謂門弟子曰：「吾何執？執御乎？執射乎？吾執御矣！」（御能引重致遠）",

"子罕之三：子曰：「麻冕，禮也；今也純，儉，吾從眾。拜下，禮也；今拜乎上，泰也。雖違眾，吾從下。」",

"子罕之四：子絕四：「毋意，毋必，毋固，毋我。」",

"子罕之五：子畏於匡。曰：「文王（文德之王）既沒，文不在茲乎？天之將喪斯文也，後死者，不得與於斯文也。天之未喪斯文也，匡人其如予何？」",

"子罕之六：大宰問於子貢曰：「夫子聖者與？何其多能也？」子貢曰：「固天縱之將聖，又多能也。」子聞之曰：「大宰知我乎！吾少也賤，故多能鄙事。君子多乎哉？不多也！」牢曰：「子云：『吾不試，故藝。』」",

"子罕之七：子曰：「吾有知乎哉？無知也。有鄙夫問於我，空空如也，我扣其兩端而竭焉。」",

"子罕之八：子曰：「鳳鳥不至，河不出圖，吾已矣夫！」",

"子罕之九：子見齊衰者，冕衣裳者，與瞽者，見之，雖少必作，過之必趨。",

"子罕之十：顏淵喟然歎曰：「仰之彌高，鑽之彌堅，瞻之在前，忽焉在後！夫子循循然善誘人：博我以文，約我以禮。欲罷不能，既竭吾才，如有所立卓爾，雖欲從之，末由也已！」",

"子罕之十一：子疾病，子路使門人為臣。病閒，曰：「久矣哉，由之行詐也！無臣而為有臣，吾誰欺？欺天乎？且予與其死於臣之手也，無寧死於二三子之手乎！且予縱不得大葬，予死於道路乎？」",

"子罕之十二：子貢曰：「有美玉於斯，韞櫝而藏諸？求善賈而沽諸？」子曰：「沽之哉！沽之哉！我待賈者也！」",

"子罕之十三：子欲居九夷。或曰：「陋，如之何？」子曰：「君子居之，何陋之有？」",

"子罕之十四：子曰：「吾自衛反魯，然後樂正，雅頌各得其所。」",

