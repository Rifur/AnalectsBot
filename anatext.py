"衛靈公之四一：師冕（古代樂師多為盲人）見。及階，子曰：「階也。」及席，子曰：「席也。」皆坐，子告之曰：「某在斯，某在斯。」師冕出，子張問曰：「與師言之道與？」子曰：「然，固相師之道也。」",


["季氏之一：季氏將伐顓臾。冉有季路見於孔子曰：「季氏將有事於顓臾。」孔子曰：「求！無乃爾是過與？夫顓臾，昔者先王以為__東蒙__主（主祭祀），且在邦域之中矣，是社稷之臣也。何以伐為？」冉有曰：「夫子（季氏）欲之，吾二臣者，皆不欲也。」",

"孔子曰：「求！__周任__（古代有名的太史）有言曰：『陳力就列，不能者止。』危而不持，顛而不扶，則將焉用彼相矣？且爾言過矣！虎兕出於柙，龜玉毀於櫝中，是誰之過與？」","冉有曰：「今夫__顓臾__，固而近於__費__（季氏封邑）；今不取，後世必為子孫憂。」",

"孔子曰：「求！君子疾夫舍曰欲之，而必為之辭。丘也，聞有國有家者，不患寡而患不均，不患貧而患不安。蓋均無貧，和無寡，安無傾。",

"夫如是，故遠人不服，則修文德以來（招徠）之。既來之，則安之。今由與求也，相夫子（季氏），遠人不服而不能來也；邦分崩離析而不能守也，而謀動干戈於邦內。吾恐季孫之憂，不在顓臾，而在蕭牆之內也！」"],

"季氏之二：孔子曰：「天下有道，則禮樂征伐自天子出；天下無道，則禮樂征伐自諸侯出。自諸侯出，蓋十世希不失矣；自大夫出，五世希不失矣；陪臣執國命，三世希不失矣。天下有道，則政不在大夫。天下有道，則庶人不議。」",

"季氏之三：孔子曰：「祿之去公室，五世矣。政逮於大夫，四世矣。故夫三桓之子孫，微矣。」",

"季氏之四：孔子曰：「益者三友，損者三友：友直，友諒，友多聞，益矣；友便辟，友善柔，友便佞，損矣。」",

"季氏之五：孔子曰：「益者三樂，損者三樂：樂節禮樂，樂道人之善，樂多賢友，益矣；樂驕樂，樂佚遊，樂宴樂，損矣。」",

"季氏之六：孔子曰：「侍於君子有三愆：言未及之而言，謂之躁；言及之而不言，謂之隱；未見顏色而言，謂之瞽。」",

"季氏之七：孔子曰：「君子有三戒：少之時，血氣未定，戒之在色；及其壯也，血氣方剛，戒之在鬥；及其老也，血氣既衰，戒之在得。」",

"季氏之八：孔子曰：「君子有三畏：畏天命，畏大人，畏聖人之言。小人不知天命而不畏也，狎大人，侮聖人之言。」",

"季氏之九：孔子曰：「生而知之者，上也；學而知之者，次也；困而學之，又其次也。困而不學，民斯為下矣！」",

"季氏之十：孔子曰：「君子有九思：視思明，聽思聰，色思溫，貌思恭，言思忠，事思敬，疑思問，忿思難，見得思義。」",

"季氏之十一：孔子曰：「『見善如不及，見不善如探湯。』吾見其人矣，吾聞其語矣。『隱居以求其志，行義以達其道。』吾聞其語矣，未見其人也。」",

"季氏之十二：（孔子曰：）齊景公有馬千駟，死之日，民無德而稱焉。伯夷叔齊餓於首陽之下，民到于今稱之（『誠不以富，亦祇以異。』）。其斯之謂與？",

["季氏之十三：陳亢問於伯魚曰：「子亦有異聞乎？」對曰：「未也。嘗獨立，鯉趨而過庭。曰：『學詩乎？』對曰：『未也。』『不學詩，無以言。』鯉退而學詩。", 

"他日，又獨立，鯉趨而過庭。曰：『學禮乎？』對曰：『未也。』『不學禮，無以立！』鯉退而學禮。聞斯二者。」陳亢退而喜曰：「問一得三：聞《詩》，聞禮，又聞君子之遠其子也。」"],

