"微子之八：逸民：伯夷、叔齊、虞仲、夷逸、朱張、柳下惠、少連。子曰：「不降其志，不辱其身，伯夷叔齊與！」謂：「柳下惠、少連，降志辱身矣。言中倫，行中慮，其斯而已矣。」謂虞仲、夷逸：「隱居放言，身中清，廢中權。」我則異於是，無可無不可。」（環境不斷在變，不變如何合於時？）",

